import Foundation
import UIKit


extension UIColor {
    
    static func radPink() -> UIColor {
        return UIColor(red:0.824, green:0.000, blue:0.267, alpha:1.00)
    }
    
    static func lightOrange() -> UIColor {
        return UIColor(red:1.000, green:0.753, blue:0.451, alpha:1.00)
    }
}